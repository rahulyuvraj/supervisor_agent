def _render_evidence_badge(confidence: str) -> str:
    css_class = f"badge-{confidence.lower()}"
    label = confidence.upper()
    return f'<span class="{css_class}">{label}</span>'


def _render_section(section: Dict, section_idx: int) -> None:
    """Render a single report section with tables and evidence."""
    title = section["title"]
    body = section["body"]

    if not body and section["level"] == 1:
        # Top-level title — render as main heading
        st.markdown(f"# {title}")
        if body:
            st.markdown(body)
        return

    # Section container
    heading_level = min(section["level"], 4)
    st.markdown(f"{'#' * heading_level} {title}", anchor=section["anchor"])

    if not body:
        st.caption("*No content for this section.*")
        return

    # Check for API-enriched tables
    api_tables = _detect_api_tables(body)

    # Extract tables from body
    text_body, tables = _extract_tables(body)

    # Render text body
    if text_body.strip():
        st.markdown(text_body)

    # Render tables
    for idx, table_data in enumerate(tables):
        if len(table_data) < 2:
            continue
        headers = table_data[0]
        rows = table_data[1:]

        # Check if this is an API-enriched table
        is_api = idx < len(api_tables)

        if is_api:
            col1, col2 = st.columns([0.95, 0.05])
            with col1:
                st.caption(api_tables[idx] if idx < len(api_tables) else "")
            with col2:
                st.markdown('<span class="badge-api">API</span>', unsafe_allow_html=True)

        import pandas as pd
        df = pd.DataFrame(rows, columns=headers)
        st.dataframe(df, use_container_width=True, hide_index=True)


def _render_conflicts(md_text: str) -> None:
    """Render cross-module conflict section if present."""
    conflict_match = re.search(
        r"## Cross-Module Conflicts\n(.+?)(?=\n## |\Z)", md_text, re.DOTALL,
    )
    if not conflict_match:
        return

    st.markdown("---")
    st.markdown("### ⚠️ Cross-Module Conflicts")
    conflicts = conflict_match.group(1).strip().split("\n")
    for c in conflicts:
        c = c.strip()
        if c.startswith("- "):
            st.markdown(
                f'<div class="conflict-box">{c[2:]}</div>',
                unsafe_allow_html=True,
            )


