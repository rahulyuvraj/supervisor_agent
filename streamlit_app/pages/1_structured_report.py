def _find_reports() -> List[Path]:
    """Find all structured report .md files."""
    search_dirs = [
        Path("agentic_ai_wf/shared/reports/structured_reports"),
        Path(project_root / "agentic_ai_wf" / "shared" / "reports" / "structured_reports"),
    ]
    reports = []
    for d in search_dirs:
        if d.exists():
            reports.extend(sorted(d.rglob("*.md"), reverse=True))

    # Also check session state for a direct path
    if "conversation" in st.session_state:
        conv = st.session_state.get("conversation")
        if conv and hasattr(conv, "workflow_state") and conv.workflow_state:
            md_path = conv.workflow_state.get("structured_report_md_path")
            if md_path:
                p = Path(md_path)
                if p.exists() and p not in reports:
                    reports.insert(0, p)

    return reports


def _read_report(path: Path) -> str:
    """Read a report file with error handling."""
    try:
        return path.read_text(encoding="utf-8")
    except Exception as e:
        return f"**Error reading report:** {e}"


