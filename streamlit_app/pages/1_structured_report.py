def _parse_markdown_sections(md_text: str) -> List[Dict]:
    """Parse a Markdown report into structured sections.

    Returns a list of dicts with keys: level, title, anchor, body.
    """
    sections = []
    current: Optional[Dict] = None
    body_lines: List[str] = []

    for line in md_text.split("\n"):
        heading_match = re.match(r"^(#{1,4})\s+(.+)", line)
        if heading_match:
            # Flush previous section
            if current is not None:
                current["body"] = "\n".join(body_lines).strip()
                sections.append(current)

            level = len(heading_match.group(1))
            title = heading_match.group(2).strip()
            anchor = re.sub(r"[^a-z0-9]+", "-", title.lower()).strip("-")
            current = {"level": level, "title": title, "anchor": anchor, "body": ""}
            body_lines = []
        else:
            body_lines.append(line)

    # Flush last section
    if current is not None:
        current["body"] = "\n".join(body_lines).strip()
        sections.append(current)

    return sections


def _extract_tables(body: str) -> Tuple[str, List[List[List[str]]]]:
    """Extract Markdown pipe tables from section body.

    Returns (body_without_tables, list_of_tables).
    Each table is [[headers], [row1], [row2], ...].
    """
    tables = []
    non_table_lines = []
    table_lines: List[str] = []
    in_table = False

    for line in body.split("\n"):
        stripped = line.strip()
        if "|" in stripped and stripped.startswith("|"):
            table_lines.append(stripped)
            in_table = True
        else:
            if in_table and table_lines:
                table = _parse_pipe_table(table_lines)
                if table:
                    tables.append(table)
                table_lines = []
                in_table = False
            non_table_lines.append(line)

    # Flush trailing table
    if table_lines:
        table = _parse_pipe_table(table_lines)
        if table:
            tables.append(table)

    return "\n".join(non_table_lines), tables


def _parse_pipe_table(lines: List[str]) -> Optional[List[List[str]]]:
    """Parse pipe-delimited Markdown table lines into rows."""
    if len(lines) < 2:
        return None
    rows = []
    for i, line in enumerate(lines):
        cells = [c.strip() for c in line.strip("|").split("|")]
        # Skip separator row (---+)
        if all(re.match(r"^-+$", c.strip()) for c in cells):
            continue
        rows.append(cells)
    return rows if len(rows) >= 1 else None


def _detect_api_tables(body: str) -> List[str]:
    """Detect table captions that came from API enrichment."""
    api_markers = []
    captions = re.findall(r"\*\*(.+?)\*\*", body)
    for cap in captions:
        lower = cap.lower()
        if any(kw in lower for kw in ("ensembl", "string", "reactome", "chembl",
                                       "openfda", "faers", "dgidb", "clinical trial")):
            api_markers.append(cap)
    return api_markers


